#include <limits.h>

int	ft_sqrt(int nb);

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (i * i <= nb && nb <= 2147395600)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
