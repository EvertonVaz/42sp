// Reproduzir de forma idêntica o funcionamento da função strlcat (man strlcat).
// ela concatena n caracteres de src a dest coloca NULL ao final da str
// retorna a soma de len de src e dest

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		str++;
		i++;
	}
	return (i);
}

